    private void initTable() {
            model = new DefaultTableModel();
            model.addColumn("Kode Obat");
            model.addColumn("Nama Obat");
            model.addColumn("Jenis Obat");
            model.addColumn("Stok");
            model.addColumn("Satuan");
            model.addColumn("Tanggal Expired");
            model.addColumn("Produsen");
            model.addColumn("Harga");
            tblOBat.setModel(model);
        }
    
    
    private void loadData() {
        model.setRowCount(0);
        String sql = "SELECT * FROM obat";
        
        try (PreparedStatement stmt = conn.prepareStatement(sql);
             ResultSet rs = stmt.executeQuery()) {
            
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("kd_obat"),
                    rs.getString("nama_obat"),
                    rs.getString("jenis_obat"),
                    rs.getInt("stok"),
                    rs.getString("satuan"),
                    rs.getDate("tanggal_expired"),
                    rs.getString("produsen"),
                    rs.getDouble("harga")
                });
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage());
        }
    }
    
    private void simpanData() {
    String sql = "INSERT INTO obat (kd_obat, nama_obat, jenis_obat, stok, satuan, tanggal_expired, produsen, harga) "
               + "VALUES (?, ?, ?, ?, ?, ?, ?, ?)";
    
    try (PreparedStatement stmt = conn.prepareStatement(sql)) {
        stmt.setString(1, kdObat.getText());
        stmt.setString(2, namaObat.getText());
        stmt.setString(3, jenisObat.getText());
        stmt.setInt(4, Integer.parseInt(stokObat.getText()));
        stmt.setString(5, satuanObat.getText());
        
        // Fixed date handling
        java.time.LocalDate localDate = tanggal.getDate();
        if (localDate != null) {
            stmt.setDate(6, java.sql.Date.valueOf(localDate));
        } else {
            stmt.setNull(6, Types.DATE);
        }
        
        stmt.setString(7, hargaProdusen.getText());
        stmt.setDouble(8, Double.parseDouble(hargaObat.getText()));
        
        stmt.executeUpdate();
        JOptionPane.showMessageDialog(this, "Data berhasil disimpan");
        loadData();
        clearForm();
    } catch (SQLException | NumberFormatException ex) {
        JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage());
    }
}
    
  private void editData() {
    // Validate required fields first
    if (kdObat.getText().isEmpty() || namaObat.getText().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Kode Obat dan Nama Obat harus diisi", 
            "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    String sql = "UPDATE obat SET nama_obat=?, jenis_obat=?, stok=?, satuan=?, "
               + "tanggal_expired=?, produsen=?, harga=? WHERE kd_obat=?";
    
    try (PreparedStatement stmt = conn.prepareStatement(sql)) {
        // Set parameters in correct order (1-7 for values, 8 for WHERE clause)
        stmt.setString(1, namaObat.getText());
        stmt.setString(2, jenisObat.getText());
        stmt.setInt(3, Integer.parseInt(stokObat.getText()));
        stmt.setString(4, satuanObat.getText());
        
        // Handle date conversion
        java.time.LocalDate localDate = tanggal.getDate();
        if (localDate != null) {
            stmt.setDate(5, java.sql.Date.valueOf(localDate));
        } else {
            stmt.setNull(5, Types.DATE);
        }
        
        stmt.setString(6, hargaProdusen.getText());
        stmt.setDouble(7, Double.parseDouble(hargaObat.getText()));
        stmt.setString(8, kdObat.getText());  // WHERE clause parameter
        
        int updatedRows = stmt.executeUpdate();
        if (updatedRows > 0) {
            JOptionPane.showMessageDialog(this, "Data berhasil diupdate");
            loadData();
            clearForm();
        } else {
            JOptionPane.showMessageDialog(this, "Data tidak ditemukan", 
                "Error", JOptionPane.WARNING_MESSAGE);
        }
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, 
            "Database Error: " + ex.getMessage(), 
            "Error", JOptionPane.ERROR_MESSAGE);
    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(this, 
            "Format angka tidak valid (stok/harga)", 
            "Error", JOptionPane.ERROR_MESSAGE);
    }
}
    
    private void hapusData() {
    int selectedRow = tblOBat.getSelectedRow();
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Pilih data yang akan dihapus");
        return;
    }
    
    // Show confirmation dialog
    int confirm = JOptionPane.showConfirmDialog(
        this,
        "Apakah Anda yakin ingin menghapus data obat ini?",
        "Konfirmasi Hapus",
        JOptionPane.YES_NO_OPTION,
        JOptionPane.WARNING_MESSAGE
    );
    
    // If user clicks No, cancel the operation
    if (confirm != JOptionPane.YES_OPTION) {
        return;
    }
    
    String kodeObat = tblOBat.getValueAt(selectedRow, 0).toString();
    String sql = "DELETE FROM obat WHERE kd_obat=?";
    
    try (PreparedStatement stmt = conn.prepareStatement(sql)) {
        stmt.setString(1, kodeObat);
        int rowsDeleted = stmt.executeUpdate();
        
        if (rowsDeleted > 0) {
            JOptionPane.showMessageDialog(this, "Data obat berhasil dihapus");
            loadData();
            clearForm();
        } else {
            JOptionPane.showMessageDialog(this, "Data obat tidak ditemukan");
        }
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, 
            "Gagal menghapus data: " + ex.getMessage(),
            "Error",
            JOptionPane.ERROR_MESSAGE);
    }
}
    
