    private void tblOBatMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblOBatMouseClicked
        int row = tblOBat.getSelectedRow();
    
    if (row >= 0) {
        // Fill the form fields
        kdObat.setText(tblOBat.getValueAt(row, 0).toString());
        namaObat.setText(tblOBat.getValueAt(row, 1).toString());
        jenisObat.setText(tblOBat.getValueAt(row, 2).toString());
        stokObat.setText(tblOBat.getValueAt(row, 3).toString());
        satuanObat.setText(tblOBat.getValueAt(row, 4).toString());
        
        // Handle date
        Object dateValue = tblOBat.getValueAt(row, 5);
        if (dateValue != null) {
            if (dateValue instanceof java.sql.Date) {
                tanggal.setDate(((java.sql.Date) dateValue).toLocalDate());
            } else if (dateValue instanceof String) {
                try {
                    java.time.LocalDate localDate = java.time.LocalDate.parse(dateValue.toString());
                    tanggal.setDate(localDate);
                } catch (Exception ex) {
                    tanggal.setDate(null);
                }
            }
        } else {
            tanggal.setDate(null);
        }
        
        hargaProdusen.setText(tblOBat.getValueAt(row, 6).toString());
        hargaObat.setText(tblOBat.getValueAt(row, 7).toString());
        
        // Make kdObat non-editable when editing
        kdObat.setEditable(false);
        kdObat.setBackground(Color.LIGHT_GRAY);
    }
    }//GEN-LAST:event_tblOBatMouseClicked

