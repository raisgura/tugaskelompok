    private void searchData() {
    String keyword = txtSearch.getText().trim();
    
    // Clear current table data
    model.setRowCount(0);
    
    String sql = "SELECT * FROM obat WHERE " +
                 "kd_obat LIKE ? OR " +
                 "nama_obat LIKE ? OR " +
                 "jenis_obat LIKE ? OR " +
                 "satuan LIKE ? OR " +
                 "produsen LIKE ?";
    
    try (PreparedStatement stmt = conn.prepareStatement(sql)) {
        // Set parameters for each searchable column
        String searchPattern = "%" + keyword + "%";
        for (int i = 1; i <= 5; i++) {
            stmt.setString(i, searchPattern);
        }
        
        ResultSet rs = stmt.executeQuery();
        
        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getString("kd_obat"),
                rs.getString("nama_obat"),
                rs.getString("jenis_obat"),
                rs.getInt("stok"),
                rs.getString("satuan"),
                rs.getDate("tanggal_expired"),
                rs.getString("produsen"),
                rs.getDouble("harga")
            });
        }
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, 
            "Error saat mencari data: " + ex.getMessage(),
            "Error",
            JOptionPane.ERROR_MESSAGE);
    }
}
    
    private String generateKodeObat() {
    String prefix = "OBT";
    String sql = "SELECT MAX(kd_obat) AS last_code FROM obat WHERE kd_obat LIKE ?";
    
    try (PreparedStatement stmt = conn.prepareStatement(sql)) {
        stmt.setString(1, prefix + "%");
        ResultSet rs = stmt.executeQuery();
        
        if (rs.next()) {
            String lastCode = rs.getString("last_code");
            if (lastCode != null) {
                int lastNumber = Integer.parseInt(lastCode.substring(prefix.length()));
                return prefix + String.format("%04d", lastNumber + 1);
            }
        }
    } catch (SQLException | NumberFormatException ex) {
        JOptionPane.showMessageDialog(this, 
            "Error generating code: " + ex.getMessage(),
            "Error",
            JOptionPane.ERROR_MESSAGE);
    }
    return prefix + "0001";
}
