    private void hapusData() {
    int selectedRow = tblPetugas.getSelectedRow();
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Pilih data yang akan dihapus");
        return;
    }
    
    // Show confirmation dialog
    int response = JOptionPane.showConfirmDialog(
        this, 
        "Anda yakin ingin menghapus data ini?", 
        "Konfirmasi Hapus",
        JOptionPane.YES_NO_OPTION,
        JOptionPane.QUESTION_MESSAGE
    );
    
    // If user clicks No, cancel the deletion
    if (response != JOptionPane.YES_OPTION) {
        return;
    }
    
    String kodePetugas = tblPetugas.getValueAt(selectedRow, 0).toString();
    String sql = "DELETE FROM petugas_medis WHERE kd_petugas=?";
    
    try (PreparedStatement stmt = conn.prepareStatement(sql)) {
        stmt.setString(1, kodePetugas);
        int rowsAffected = stmt.executeUpdate();
        
        if (rowsAffected > 0) {
            JOptionPane.showMessageDialog(this, "Data berhasil dihapus");
            loadData();
            clearForm();
        } else {
            JOptionPane.showMessageDialog(this, "Data gagal dihapus");
        }
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, 
            "Error: " + ex.getMessage(),
            "Error",
            JOptionPane.ERROR_MESSAGE);
    }
}
    
