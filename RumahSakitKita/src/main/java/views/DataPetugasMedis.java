    private void searchData() {
    String keyword = txtSearch.getText().trim();
    model.setRowCount(0);
    
    String sql = "SELECT * FROM petugas_medis WHERE "
            + "kd_petugas LIKE ? OR "
            + "nik LIKE ? OR "
            + "nama_petugas LIKE ? OR "
            + "profesi LIKE ? OR "
            + "no_hp LIKE ?";
    
    try (PreparedStatement stmt = conn.prepareStatement(sql)) {
        String searchPattern = "%" + keyword + "%";
        for (int i = 1; i <= 5; i++) {
            stmt.setString(i, searchPattern);
        }
        
        ResultSet rs = stmt.executeQuery();
        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getString("kd_petugas"),
                rs.getString("nik"),
                rs.getString("nama_petugas"),
                rs.getString("profesi"),
                rs.getString("jenis_kelamin"),
                rs.getString("alamat"),
                rs.getString("no_hp"),
                rs.getDate("tanggal_lahir"),
                rs.getString("status_akhir")
            });
        }
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, 
            "Error saat mencari data: " + ex.getMessage(),
            "Error",
            JOptionPane.ERROR_MESSAGE);
    }
}
    
    private String generateKodePetugas() {
    String prefix = "PTG";
    String sql = "SELECT MAX(kd_petugas) AS last_code FROM petugas_medis WHERE kd_petugas LIKE ?";
    
    try (PreparedStatement stmt = conn.prepareStatement(sql)) {
        stmt.setString(1, prefix + "%");
        ResultSet rs = stmt.executeQuery();
        
        if (rs.next()) {
            String lastCode = rs.getString("last_code");
            if (lastCode != null) {
                int lastNumber = Integer.parseInt(lastCode.substring(prefix.length()));
                return prefix + String.format("%04d", lastNumber + 1);
            }
        }
    } catch (SQLException | NumberFormatException ex) {
        JOptionPane.showMessageDialog(this, 
            "Error generating code: " + ex.getMessage(),
            "Error",
            JOptionPane.ERROR_MESSAGE);
    }
    return prefix + "0001";
}
