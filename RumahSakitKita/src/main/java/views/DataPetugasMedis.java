    private void tblPetugasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPetugasMouseClicked
        int row = tblPetugas.getSelectedRow();
    
    if (row >= 0) {
        // Fill the form fields
        kdPetugas.setText(tblPetugas.getValueAt(row, 0).toString());
        nikPetugas.setText(tblPetugas.getValueAt(row, 1).toString());
        namaPetugas.setText(tblPetugas.getValueAt(row, 2).toString());
        
        // Set profesi
        String profesi = tblPetugas.getValueAt(row, 3).toString();
        profesiPetugas.setSelectedItem(profesi);
        
        // Set gender
        String jk = tblPetugas.getValueAt(row, 4).toString();
        if (jk.equals("L")) {
            jkL.setSelected(true);
        } else {
            jkP.setSelected(true);
        }
        
        alamatPetugas.setText(tblPetugas.getValueAt(row, 5).toString());
        nohpPetugas.setText(tblPetugas.getValueAt(row, 6).toString());
        
        // Handle date
        Object dateValue = tblPetugas.getValueAt(row, 7);
        if (dateValue != null) {
            if (dateValue instanceof java.sql.Date) {
                tglLahir.setDate(((java.sql.Date) dateValue).toLocalDate());
            } else if (dateValue instanceof String) {
                try {
                    java.time.LocalDate localDate = java.time.LocalDate.parse(dateValue.toString());
                    tglLahir.setDate(localDate);
                } catch (Exception ex) {
                    tglLahir.setDate(null);
                }
            }
        } else {
            tglLahir.setDate(null);
        }
        
        // Set status
        String status = tblPetugas.getValueAt(row, 8).toString();
        statusPetugas.setSelectedItem(status);
        
        // Make kdPetugas non-editable when editing
        kdPetugas.setEditable(false);
        kdPetugas.setBackground(Color.LIGHT_GRAY);
    }
    }//GEN-LAST:event_tblPetugasMouseClicked

